let currentLang = 'zh-TW';

// Auto-detect browser language on page load
function detectBrowserLanguage() {
  const browserLang = navigator.language || navigator.userLanguage;
  const langMap = {
    'zh-TW': 'zh-TW',
    'zh-HK': 'zh-TW',
    'zh-MO': 'zh-TW',
    'zh-CN': 'zh-CN',
    'zh-SG': 'zh-CN',
    'en-US': 'en',
    'en-GB': 'en',
    'en-AU': 'en',
    'ja-JP': 'ja',
    'ko-KR': 'ko'
  };
  
  // Check localStorage first (user preference)
  const savedLang = localStorage.getItem('preferred-language');
  if (savedLang && translations[savedLang]) {
    return savedLang;
  }
  
  // Then check browser language
  const detected = langMap[browserLang] || langMap[browserLang.substring(0, 2)] || 'zh-TW';
  return detected;
}

function setLanguage(lang) {
  if (!translations[lang]) {
    console.warn(`Language '${lang}' not supported, falling back to zh-TW`);
    lang = 'zh-TW';
  }
  
  currentLang = lang;
  document.documentElement.lang = lang;
  
  // Save user preference
  localStorage.setItem('preferred-language', lang);
  
  // Update all elements with data-i18n attribute
  updatePageContent();
  
  // Dispatch custom event for other scripts to listen
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
  
  console.log(`Language changed to: ${lang}`);
}

function t(key) {
  return translations[currentLang]?.[key] || translations['zh-TW']?.[key] || key;
}

function updatePageContent() {
  // Update text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key);
    
    if (translation && translation !== key) {
      // Check if element has HTML content (contains <br> tags)
      if (translation.includes('<br>')) {
        el.innerHTML = translation;
      } else {
        el.textContent = translation;
      }
    }
  });
  
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const translation = t(key);
    if (translation && translation !== key) {
      el.placeholder = translation;
    }
  });
  
  // Update aria-labels
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    const translation = t(key);
    if (translation && translation !== key) {
      el.setAttribute('aria-label', translation);
    }
  });
  
  // Update active state on language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const btnLang = btn.getAttribute('data-lang');
    if (btnLang === currentLang) {
      btn.style.background = 'rgba(123,47,190,0.3)';
      btn.style.borderColor = 'rgba(123,47,190,0.6)';
      btn.style.color = 'white';
      btn.style.fontWeight = '700';
    } else {
      btn.style.background = 'transparent';
      btn.style.borderColor = 'rgba(255,255,255,0.2)';
      btn.style.color = 'rgba(255,255,255,0.6)';
      btn.style.fontWeight = '500';
    }
  });
}

// Initialize language system when DOM is ready
function initI18n() {
  const detectedLang = detectBrowserLanguage();
  setLanguage(detectedLang);
  
  // Bind language switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', function(e) {
      e.preventDefault();
      e.stopPropagation();
      const lang = this.getAttribute('data-lang');
      if (lang) {
        setLanguage(lang);
        // Close dropdown after selection
        const dropdown = document.getElementById('langDropdown');
        if (dropdown) {
          dropdown.style.display = 'none';
        }
      }
    });
  });
  
  // Language toggle button
  const langToggle = document.getElementById('langToggle');
  const langDropdown = document.getElementById('langDropdown');
  
  if (langToggle && langDropdown) {
    langToggle.addEventListener('click', function(e) {
      e.stopPropagation();
      langDropdown.style.display = langDropdown.style.display === 'none' ? 'block' : 'none';
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', function(e) {
      if (!langToggle.contains(e.target) && !langDropdown.contains(e.target)) {
        langDropdown.style.display = 'none';
      }
    });
  }
  
  console.log('i18n system initialized with language:', currentLang);
}

// Auto-initialize when DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initI18n);
} else {
  initI18n();
}

// Export functions globally
window.translations = translations;
window.currentLang = () => currentLang;
window.setLanguage = setLanguage;
window.t = t;
window.detectBrowserLanguage = detectBrowserLanguage;
